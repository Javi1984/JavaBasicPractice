package basicPractice;

public class IntBitCountExamples {
    public static void main(String[] args){
        // returns the number of 1 bits in 2s complement in binary rep of integer
        System.out.println("Examples of Integer bitcount function");
        System.out.println(Integer.bitCount(1));
        System.out.println(Integer.bitCount(2));
        System.out.println(Integer.bitCount(5));
        System.out.println(Integer.bitCount(10));
        System.out.println(Integer.bitCount(50));
    }
}
